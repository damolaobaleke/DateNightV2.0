package com.ltd_immersia_datenight.views.onboarding;

import android.content.Intent;
import android.os.Bundle;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import androidx.annotation.NonNull;
import androidx.annotation.Nullable;
import androidx.fragment.app.Fragment;

import com.ltd_immersia_datenight.R;
import com.ltd_immersia_datenight.views.landing_screen.BoardingScreen;

public class OnBoardingFrag2 extends Fragment implements View.OnClickListener {

    @Nullable
    @Override
    public View onCreateView(@NonNull LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        View view  = inflater.inflate(R.layout.page_onboarding_two,container,false);
        TextView skipText = view.findViewById(R.id.skip_text);
        skipText.setOnClickListener(this);

        return view;
    }

    @Override
    public void onViewCreated(@NonNull View view, @Nullable Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);
    }

    public void skip(){
        Intent intent  =  new Intent(requireContext(), BoardingScreen.class);
        startActivity(intent);
    }

    @Override
    public void onClick(View v) {
        skip();
    }
}
